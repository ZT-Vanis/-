'''
函数的概念：
       如果在开发程序时，需要某块代码多次，但是为了提高编写的效率以及代码的重用，所以
    把具有独立功能的代码块组织为一个小模块，这就是函数
'''

'''
函数的定义：
   1.无参函数定义：def 函数名()：
                     代码
   2.带参函数定义：def 函数名(形参):
                     代码
   3.带返回值的函数：def 函数名():         -可带参也可不带参
                     return value      -返回值但不打印出来，可用多个值接收返回的多个值
'''

'''
函数的调用：
   1.无参函数调用：函数名()
   2.带参函数调用：函数名(实参)
'''

