#小作业2：写一个函数，可以通过输入的参数，打印出自定义函数的横线。
def ptline(num):
    number = 1
    while number <= num:
        line()
        number += 1

num = int(input("请输入要打印的横条数量:"))
ptline(num)
print("-"*5,"第二题结束","-"*5,"\n")

